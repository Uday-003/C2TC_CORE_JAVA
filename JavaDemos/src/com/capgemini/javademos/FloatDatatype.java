//This program demonstrate floating value variables
package com.capgemini.javademos;

public class FloatDatatype {

	public static void main(String[] args) {
		
		int firstInt = 9/2;
		float firstFloatVal = 10f/6f;//Floating value is defined
		double firstDoubleVal = 10d/6d;//double value is defined
		System.out.println("value  1 = "+ firstInt);
		System.out.println("value  2 = "+ firstFloatVal);
		System.out.println("value  3 = "+ firstDoubleVal);
		
	    int marker = 512;
	    double percentage = marker * 0.46f;
	    
	    System.out.println("percentage:"+percentage);


	}

}
