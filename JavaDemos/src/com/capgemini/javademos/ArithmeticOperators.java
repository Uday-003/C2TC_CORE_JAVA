//This program demonstrates arithmetic operators
package com.capgemini.javademos;

public class ArithmeticOperators {

	public static void main(String[] args) {
		int firstVal = 10, secondVal=20;
		System.out.println(firstVal+secondVal);//this is '+' operator
		System.out.println(--firstVal);//this is '--' operator
		System.out.println(++secondVal);//this is '+' operator

	}

}
